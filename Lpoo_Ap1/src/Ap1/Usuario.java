package Ap1;

public class Usuario {

		private String Nome;
		private int idade;
		
		public String getNome() {
			return Nome;
		}
		public void setNome(String novoNome) {
			Nome = novoNome;
		}
		public int getIdade() {
			return idade;
		}
		public void setIdade(int novaIdade) {
			idade = novaIdade;
		}
}
